    写好代码的101个方法：   
    1.编写经得起修改的代码
        (1)Keep it simple或者是keep it short and simple,代码保持简洁
        (2)现在用不到的代码就不应该现在写，因为在大多数情况下，将来也用不到
        (3)不用擅自添加需求（需求不是我们确定的，是用户确定的）
        (4)奥卡姆剃刀，当一个食物存在多种解释时，最简单那个往往是对的

    2.DRY(Dont repeat yourself)，严禁复制粘贴代码
        (1)条件相同的控制语句的代码块
        (2)将常量写入代码里
        (3)代码的注释


